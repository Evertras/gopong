//+build ignore

package main

import (
	"log"
	"os"

	"github.com/Evertras/go-ts-type-generator/typegen"
	"github.com/Evertras/gopong/lib/state/starting"
)

func main() {
	generator := typegen.NewWithConfig(typegen.Config{
		Indentation: "    ",
		Prefix:      "MessageStarting",
	})

	outfile, err := os.Create("../../../front/src/game/states/starting/messageTypes.ts")

	if err != nil {
		log.Panic(err)
	}

	defer outfile.Close()

	outfile.WriteString("/* THIS FILE IS AUTOMATICALLY GENERATED, DO NOT EDIT */\n\n")

	err = generator.GenerateTypes(
		outfile,
		starting.State{},
	)

	if err != nil {
		log.Fatal(err)
	}

	outfile.WriteString("\n")
}
